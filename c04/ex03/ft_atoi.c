#include <unistd.h>

int	ft_atoi(char *str)
{
	unsigned int	i;
	unsigned int	nb;
	int				k;

	k = 1;
	i = 0;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			k = k * -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] -48;
		i++;
	}
	return (nb * k);
}
