int	ft_str_is_printable(char *str)
{
	int	i;
	int	k;

	i = 1;
	k = 0;
	while (str[k] != '\0')
	{
		if (str[k] >= 32 && str[k] <= 127)
			i = 1;
		else
		{
			i = 0;
			return (i);
		}
		k++;
	}
	return (i);
}
