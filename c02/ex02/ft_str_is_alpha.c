int	ft_str_is_alpha(char *str)
{
	int	i;
	int	k;

	i = 1;
	k = 0;
	while (str[k] != '\0')
	{
		if (str[k] >= 'A' && str[k] <= 'Z')
			i = 1;
		else if ((str[k] >= 'a' && str[k] <= 'z') || str[k] == ' ')
			i = 1;
		else
		{
			i = 0;
			return (i);
		}	
		k++;
	}
	return (i);
}
